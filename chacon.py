# cha hon con it nhat la 25 tuoi
# khi nao tuoi cha gap doi tuoi con


def khi_nao(tuoi_cha, tuoi_con):
    # tai thoi diem tuoi cha = 2 lan tuoi con
    # luc do goi tuoi con la x, tuoi cha la 2x
    x = tuoi_cha - tuoi_con
    tuoi_cha_khi_do = 2 * x

    so_nam = tuoi_cha_khi_do - tuoi_cha
    return so_nam
